/*

variables are used to store any type of data

We will create a variable using var/let/const 

syntax:

var/let/const <variablename>:datatype=value
var var1:number=100;

console.log ==> Function which will dispaly the output on the command console/browser console
*/

var var1:number=100; //Decimal Value
var var2:number=100.1234 //Float Value
var var3:number=0x123ABC; //Hexa Decimal
var var4:number=0o123; //Octal System
var var5:number=0b010111; //Binary System

console.log("Decimal Value ==> ",var1);
console.log("Float Value ==> ",var2);
console.log("Hexa Decimal Value ==> ",var3);
console.log("Octal Value ==> ",var4);
console.log("Binary Value ==> ",var5);

/*

Output

Decimal Value ==>  100
Float Value ==>  100.1234
Hexa Decimal Value ==>  1194684
Octal Value ==>  83
Binary Value ==>  23
*/






